def fig_bert_delta_from_pretrained():
    comparisons = [
        ("bert_finetuned", "BERT-FT - BERT"),
        ("bert_lora", "LoRA - BERT"),
    ]

    if not all(corrs_path(subject, "bert_pretrained").exists() for subject in SUBJECTS):
        print("Skipping delta figure: pretrained BERT correlations are missing.")
        return

    fig, axes = plt.subplots(2, len(SUBJECTS), figsize=(13.5, 7.6), sharex=True, constrained_layout=True)
    if len(SUBJECTS) == 1:
        axes = axes[:, None]

    bins = np.linspace(-0.08, 0.08, 90)
    for col, subject in enumerate(SUBJECTS):
        base = np.asarray(load_corrs(subject, "bert_pretrained"), dtype=np.float32)
        for row, (embedding, label) in enumerate(comparisons):
            ax = axes[row, col]
            corrs = load_corrs(subject, embedding)
            if corrs is None:
                ax.set_visible(False)
                continue
            delta = np.asarray(corrs, dtype=np.float32) - base
            improved = 100.0 * float((delta > 0).mean())
            mean_delta = float(delta.mean())

            ax.hist(delta, bins=bins, color=COLORS[embedding], alpha=0.78, density=True)
            ax.axvline(0, color="#111111", linewidth=1.1)
            ax.axvline(mean_delta, color="#FFFFFF", linewidth=2.2)
            ax.axvline(mean_delta, color="#111111", linewidth=0.9, linestyle="--")
            ax.set_title(f"{SUBJECT_LABELS[subject]}: {label}", fontsize=12, fontweight="bold")
            ax.set_xlabel("Change in voxel CC")
            ax.set_ylabel("Density")
            ax.text(
                0.02,
                0.92,
                f"mean delta = {mean_delta:+.4f}\nvoxels improved = {improved:.1f}%",
                transform=ax.transAxes,
                va="top",
                ha="left",
                fontsize=9.5,
                bbox={"facecolor": "white", "edgecolor": "#D9D9D9", "boxstyle": "round,pad=0.28"},
            )

    fig.suptitle("Does Fine-Tuning Improve Individual Voxels?", fontsize=16, fontweight="bold")
    save_figure(fig, "fig3_bert_delta_from_pretrained")


def fig_well_predicted_voxel_overlap():
    embeddings = [e for e in BERT_METHODS if all(corrs_path(s, e).exists() for s in SUBJECTS)]
    if len(embeddings) < 2:
        print("Skipping well-predicted overlap: need at least two BERT correlation files for both subjects.")
        return

    fig, axes = plt.subplots(1, len(SUBJECTS), figsize=(7.2 * len(SUBJECTS), 5.2), constrained_layout=True)
    if len(SUBJECTS) == 1:
        axes = [axes]

    patterns = [
        ("BERT only", (1, 0, 0)),
        ("BERT-FT only", (0, 1, 0)),
        ("LoRA only", (0, 0, 1)),
        ("BERT + BERT-FT", (1, 1, 0)),
        ("BERT + LoRA", (1, 0, 1)),
        ("BERT-FT + LoRA", (0, 1, 1)),
        ("All three", (1, 1, 1)),
    ]

    for ax, subject in zip(axes, SUBJECTS):
        masks = []
        for embedding in BERT_METHODS:
            corrs = load_corrs(subject, embedding)
            if corrs is None:
                masks.append(np.zeros(0, dtype=bool))
            else:
                masks.append(np.asarray(corrs) >= WELL_PREDICTED_THRESHOLD)
        n = min(len(mask) for mask in masks)
        masks = [mask[:n] for mask in masks]

        counts = []
        for _, pattern in patterns:
            keep = np.ones(n, dtype=bool)
            for mask, flag in zip(masks, pattern):
                keep &= mask if flag else ~mask
            counts.append(int(keep.sum()))

        colors = ["#4C86BD", "#2D5F93", "#5BAFA3", "#6B8FBF", "#6FB4C0", "#4F8F9A", "#303030"]
        y = np.arange(len(patterns))
        ax.barh(y, counts, color=colors, edgecolor="white", linewidth=0.7)
        ax.set_yticks(y)
        ax.set_yticklabels([p[0] for p in patterns])
        ax.invert_yaxis()
        ax.set_xlabel(f"Voxels with CC >= {WELL_PREDICTED_THRESHOLD:.2f}")
        ax.set_title(SUBJECT_LABELS[subject], fontsize=13, fontweight="bold")
        ax.grid(True, axis="x", alpha=0.25)
        ax.grid(False, axis="y")
        for yi, count in zip(y, counts):
            ax.text(count + max(counts + [1]) * 0.015, yi, str(count), va="center", fontsize=9)

    fig.suptitle("Overlap of Well-Predicted Voxels Across BERT Variants", fontsize=16, fontweight="bold")
    save_figure(fig, "fig5_well_predicted_voxel_overlap")


def fig_finetuning_win_loss_summary():
    comparisons = [("bert_finetuned", "BERT-FT vs BERT"), ("bert_lora", "LoRA vs BERT")]
    if not all(corrs_path(subject, "bert_pretrained").exists() for subject in SUBJECTS):
        print("Skipping win/loss summary: pretrained BERT correlations are missing.")
        return

    fig, axes = plt.subplots(1, len(SUBJECTS), figsize=(7.0 * len(SUBJECTS), 5.0), sharey=True, constrained_layout=True)
    if len(SUBJECTS) == 1:
        axes = [axes]

    labels = ["Improved", "Unchanged", "Worse"]
    colors = ["#2E8B57", "#BDBDBD", "#C44E52"]

    for ax, subject in zip(axes, SUBJECTS):
        base = np.asarray(load_corrs(subject, "bert_pretrained"), dtype=np.float32)
        x = np.arange(len(comparisons))
        bottom = np.zeros(len(comparisons))
        for label, color, category in zip(labels, colors, ["improved", "same", "worse"]):
            vals = []
            for embedding, _ in comparisons:
                corrs = load_corrs(subject, embedding)
                if corrs is None:
                    vals.append(np.nan)
                    continue
                delta = np.asarray(corrs, dtype=np.float32)[: len(base)] - base[: len(corrs)]
                eps = 1e-6
                if category == "improved":
                    vals.append(100.0 * float((delta > eps).mean()))
                elif category == "same":
                    vals.append(100.0 * float((np.abs(delta) <= eps).mean()))
                else:
                    vals.append(100.0 * float((delta < -eps).mean()))
            ax.bar(x, vals, bottom=bottom, color=color, edgecolor="white", linewidth=0.7, label=label)
            bottom += np.nan_to_num(vals)

        ax.set_xticks(x)
        ax.set_xticklabels([name for _, name in comparisons], rotation=15, ha="right")
        ax.set_ylabel("Percent of voxels")
        ax.set_ylim(0, 100)
        ax.set_title(SUBJECT_LABELS[subject], fontsize=13, fontweight="bold")
        ax.grid(True, axis="y", alpha=0.25)
        ax.grid(False, axis="x")

    handles = [Patch(facecolor=c, edgecolor="white", label=l) for l, c in zip(labels, colors)]
    fig.legend(handles=handles, loc="lower center", ncol=3, frameon=False, bbox_to_anchor=(0.5, -0.04))
    fig.suptitle("Fine-Tuning Win/Loss Relative to Pretrained BERT", fontsize=16, fontweight="bold")
    save_figure(fig, "fig5_finetuning_win_loss_summary")


