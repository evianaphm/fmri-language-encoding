def metric_value(subject: str, embedding: str, metric: str) -> float:
    """Return one saved CC summary metric, or NaN when that run is unavailable."""
    stats = load_stats(subject, embedding)
    if stats is None:
        return np.nan
    return float(stats[metric])


def clean_axis(ax):
    """Apply the shared light-grid style used across Lab 3.2 figures."""
    ax.grid(True, axis="x", alpha=0.22)
    ax.grid(False, axis="y")


def add_context_split(ax, y_pos=None, orientation="horizontal"):
    """Draw the visual separator between static and contextual embeddings."""
    if orientation == "horizontal" and y_pos is not None:
        ax.axhline(y_pos, color="#BDBDBD", linestyle="--", linewidth=1.0, alpha=0.85)
    elif orientation == "vertical" and y_pos is not None:
        ax.axvline(y_pos, color="#BDBDBD", linestyle="--", linewidth=1.0, alpha=0.85)


def ridge_dir_for_embedding(embedding: str) -> Path:
    """Map an embedding name to the ridge output directory that owns it."""
    return RIDGE31_DIR if embedding in EMBEDDINGS_31 else RIDGE32_DIR


def stats_path(subject: str, embedding: str) -> Path:
    """Path to the JSON summary generated by the ridge script."""
    return ridge_dir_for_embedding(embedding) / f"{embedding}_{subject}_stats.json"


def corrs_path(subject: str, embedding: str) -> Path:
    """Path to voxel-wise held-out correlations for one subject/embedding."""
    return ridge_dir_for_embedding(embedding) / f"{embedding}_{subject}_corrs.npy"


def valphas_path(subject: str, embedding: str) -> Path:
    """Path to voxel-wise selected ridge alphas for one subject/embedding."""
    return ridge_dir_for_embedding(embedding) / f"{embedding}_{subject}_valphas.npy"


def load_stats(subject: str, embedding: str):
    """Load saved aggregate CC metrics; return None if the run is missing."""
    path = stats_path(subject, embedding)
    if not path.exists():
        return None
    with open(path) as fh:
        return json.load(fh)


def load_corrs(subject: str, embedding: str):
    """Memory-map voxel-wise CCs so large arrays are not copied unnecessarily."""
    path = corrs_path(subject, embedding)
    if not path.exists():
        return None
    return np.load(path, mmap_mode="r")


def load_valphas(subject: str, embedding: str):
    """Load selected ridge alphas for diagnostic plots."""
    path = valphas_path(subject, embedding)
    if not path.exists():
        return None
    return np.load(path, mmap_mode="r")


def available_embeddings(embeddings):
    """Filter a candidate embedding list to runs with at least one subject present."""
    available = []
    for embedding in embeddings:
        if any(stats_path(subject, embedding).exists() for subject in SUBJECTS):
            available.append(embedding)
    return available


def save_figure(fig, name: str):
    """Save each figure as both PNG for reports and PDF for high-quality export."""
    png = FIG_DIR / f"{name}.png"
    pdf = FIG_DIR / f"{name}.pdf"
    fig.savefig(png)
    fig.savefig(pdf)
    plt.close(fig)
    print(f"Saved: {png}")
    print(f"Saved: {pdf}")


def write_metric_tables():
    """Write compact CSV tables used directly in the report."""
    bert_rows = []
    all_rows = []

    for embedding in EMBEDDINGS_ALL:
        for subject in SUBJECTS:
            stats = load_stats(subject, embedding)
            if stats is None:
                continue
            row = {
                "subject": subject,
                "embedding": EMBEDDING_LABELS[embedding],
                "mean_cc": stats["mean_cc"],
                "median_cc": stats["median_cc"],
                "top5_cc": stats["top5_cc"],
                "top1_cc": stats["top1_cc"],
                "frac_pos": stats.get("frac_pos", np.nan),
            }
            all_rows.append(row)
            if embedding in EMBEDDINGS_32:
                bert_rows.append(row)

    def write_csv(path: Path, rows):
        with open(path, "w") as fh:
            fh.write("subject,embedding,mean_cc,median_cc,top5_cc,top1_cc,frac_pos\n")
            for row in rows:
                fh.write(
                    f"{row['subject']},{row['embedding']},"
                    f"{row['mean_cc']:.6f},{row['median_cc']:.6f},"
                    f"{row['top5_cc']:.6f},{row['top1_cc']:.6f},"
                    f"{row['frac_pos']:.6f}\n"
                )
        print(f"Saved: {path}")

    write_csv(METRIC_DIR / "cc_scores_32.csv", bert_rows)
    write_csv(METRIC_DIR / "cc_scores_all_embeddings.csv", all_rows)

    if bert_rows:
        print("\nLab 3.2 CC metrics")
        print(f"{'Subject':<12} {'Embedding':<16} {'Mean CC':>9} {'Median':>9} {'Top 5%':>9} {'Top 1%':>9}")
        print("-" * 72)
        for row in bert_rows:
            print(
                f"{row['subject']:<12} {row['embedding']:<16} "
                f"{row['mean_cc']:>9.4f} {row['median_cc']:>9.4f} "
                f"{row['top5_cc']:>9.4f} {row['top1_cc']:>9.4f}"
            )


def fig_bert_metric_summary():
    embeddings = available_embeddings(EMBEDDINGS_32)
    if not embeddings:
        print("Skipping BERT metric summary: no Lab 3.2 stats found.")
        return

    fig, axes = plt.subplots(1, 4, figsize=(17, 4.7), constrained_layout=True)
    x = np.arange(len(SUBJECTS))
    width = 0.24
    offsets = np.linspace(-width, width, len(embeddings))

    for ax, metric in zip(axes, METRICS):
        for offset, embedding in zip(offsets, embeddings):
            vals = [metric_value(subject, embedding, metric) for subject in SUBJECTS]
            bars = ax.bar(
                x + offset,
                vals,
                width=width,
                color=COLORS[embedding],
                edgecolor="white",
                linewidth=0.7,
                label=EMBEDDING_LABELS[embedding],
            )
            for bar, val in zip(bars, vals):
                if np.isfinite(val):
                    ax.text(
                        bar.get_x() + bar.get_width() / 2,
                        bar.get_height() + max(vals) * 0.025,
                        f"{val:.3f}",
                        ha="center",
                        va="bottom",
                        fontsize=7.8,
                    )

        ax.set_title(METRIC_LABELS[metric], fontsize=12, fontweight="bold")
        ax.set_xticks(x)
        ax.set_xticklabels([SUBJECT_LABELS[s] for s in SUBJECTS])
        ax.set_ylabel("Pearson r")
        ax.margins(y=0.24)
        ax.grid(True, axis="y", alpha=0.25)
        ax.grid(False, axis="x")

    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="lower center", ncol=len(embeddings), frameon=False, bbox_to_anchor=(0.5, -0.06))
    fig.suptitle("Lab 3.2 BERT Encoding Performance", fontsize=16, fontweight="bold")
    save_figure(fig, "fig1_bert_metric_summary")


