def fig_bert_voxel_distributions():
    embeddings = available_embeddings(EMBEDDINGS_32)
    if not embeddings:
        print("Skipping BERT distributions: no Lab 3.2 correlations found.")
        return

    fig, axes = plt.subplots(1, len(SUBJECTS), figsize=(13.5, 5.2), sharey=True, constrained_layout=True)
    if len(SUBJECTS) == 1:
        axes = [axes]

    for ax, subject in zip(axes, SUBJECTS):
        data = []
        labels = []
        colors = []
        for embedding in embeddings:
            corrs = load_corrs(subject, embedding)
            if corrs is None:
                continue
            data.append(np.asarray(corrs, dtype=np.float32))
            labels.append(EMBEDDING_LABELS[embedding])
            colors.append(COLORS[embedding])

        if not data:
            ax.set_visible(False)
            continue

        box = ax.boxplot(
            data,
            patch_artist=True,
            showfliers=False,
            widths=0.58,
            medianprops={"color": "#111111", "linewidth": 1.6},
            whiskerprops={"color": "#555555", "linewidth": 1.0},
            capprops={"color": "#555555", "linewidth": 1.0},
        )
        for patch, color in zip(box["boxes"], colors):
            patch.set_facecolor(color)
            patch.set_edgecolor("#2A2A2A")
            patch.set_alpha(0.75)

        for i, corrs in enumerate(data, start=1):
            p95 = np.percentile(corrs, 95)
            p99 = np.percentile(corrs, 99)
            ax.scatter([i], [p95], marker="D", s=34, color="white", edgecolor="#111111", zorder=3)
            ax.scatter([i], [p99], marker="^", s=44, color="#111111", edgecolor="white", zorder=3)
            ax.text(i + 0.08, p99, f"{p99:.3f}", fontsize=8.5, va="center")

        ax.axhline(0.10, color="#D00000", linestyle="--", linewidth=1.1, alpha=0.8)
        ax.text(0.55, 0.103, "CC = 0.10", color="#D00000", fontsize=8.5, va="bottom")
        ax.set_title(SUBJECT_LABELS[subject], fontsize=13, fontweight="bold")
        ax.set_xticks(np.arange(1, len(labels) + 1))
        ax.set_xticklabels(labels, rotation=20, ha="right")
        ax.set_ylabel("Voxel-wise held-out CC")
        ax.set_ylim(-0.18, 0.28)

    fig.suptitle("Voxel-Wise Predictive Accuracy for BERT Variants", fontsize=16, fontweight="bold")
    save_figure(fig, "fig2_bert_voxel_distributions")
    save_figure_alias("fig2_bert_voxel_distributions", "cc_distributions_bert")


