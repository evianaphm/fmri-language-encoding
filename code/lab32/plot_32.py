def best_static_corrs(subject: str):
    static_corrs = []
    labels = []
    for embedding in ["glove", "word2vec"]:
        corrs = load_corrs(subject, embedding)
        if corrs is not None:
            static_corrs.append(np.asarray(corrs, dtype=np.float32))
            labels.append(EMBEDDING_LABELS[embedding])
    if not static_corrs:
        return None, None
    n = min(len(c) for c in static_corrs)
    arr = np.vstack([c[:n] for c in static_corrs])
    best_idx = np.argmax(arr, axis=0)
    best = arr[best_idx, np.arange(n)]
    return best.astype(np.float32), labels


def context_gain_values(subject: str, embedding: str):
    static, _ = best_static_corrs(subject)
    contextual = load_corrs(subject, embedding)
    if static is None or contextual is None:
        return None
    contextual = np.asarray(contextual, dtype=np.float32)
    n = min(len(static), len(contextual))
    return contextual[:n] - static[:n]


